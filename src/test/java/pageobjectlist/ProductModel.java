package pageobjectlist;

public class ProductModel {
    private String price;
    private String name;

    public ProductModel() {}

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
